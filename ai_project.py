dir_path = '/content/drive/MyDrive/Fruits Classification'
categories = ['Apple','Banana','Grape','Mango','Strawberry']
data = []

for i in categories:

    paths = os.path.join(dir_path,i)
    print('paths',paths)
    ii=0
    for j in os.listdir(paths):
        img_path = os.path.join(paths,j)
        labels = categories.index(i)

        if (os.path.splitext(img_path)[1]=='.jpeg' or os.path.splitext(img_path)[1]=='.png'):
          img = cv2.imread(img_path)
          img = cv2.resize(img,(32,32))
          data.append([img,labels])


random.shuffle(data)
print(len(data))

x = []
y = []

for features,label in data:
    x.append(features)
    y.append(label)

#Converting lists into numpy arrays
x = np.array(x)
y = np.array(y)
x = x/255.0
x = np.array(x).reshape(-1, 32, 32, 3)
print("Shape of train images is:", x.shape)
print("Shape of labels is:", y.shape)
print(y)
print(x.shape[1:])

model = Sequential()
model.add(Conv2D(64,(3,3), activation='relu', input_shape = (x.shape[1:])))
model.add(MaxPooling2D(pool_size=(2,2)))

model.add(Conv2D(128,(3,3), activation='relu'))
model.add(MaxPooling2D(pool_size=(2,2)))

model.add(Conv2D(128,(3,3), activation='relu'))
model.add(MaxPooling2D(pool_size=(2,2)))

model.add(Dropout(0.3))
model.add(Flatten())

model.add(Dense(256,activation='relu'))
model.add(Dropout(0.3))
model.add(Dense(5,activation='softmax'))
model.summary()

model.compile(optimizer='adam',
              loss='sparse_categorical_crossentropy',
              metrics=['accuracy'])
model.fit(x, y, epochs=50,batch_size=10,validation_split=0.2)

